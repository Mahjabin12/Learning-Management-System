const makeCourse = ({
  id,
  title,
  instructor,
  category,
  level,
  price,
  oldPrice,
  rating,
  students,
  hours,
  thumbnail,
  tools = [],
  status = "Published",
}) => ({
  id,
  title,
  instructor,
  instructorBio: `${instructor} is an experienced ${category} mentor who teaches practical, job-ready and freelance-ready creative skills.`,
  category,
  level,
  price,
  oldPrice,
  rating,
  students,
  hours,
  duration: `${hours} hours`,
  status,
  tools,
  thumbnail,
  description: `Learn ${title.toLowerCase()} through practical lessons, real-world projects, portfolio work, and career-focused guidance. This course is designed to help learners build skills they can use for freelancing, jobs, client work, and business growth.`,
  whatYouLearn: [
    `Build real-world ${category} projects from scratch`,
    "Understand professional design workflow and client expectations",
    "Create portfolio-ready work with practical exercises",
    "Learn how to present your work confidently",
    "Use the skill for freelancing, jobs, or business growth",
    "Get lifetime access and certificate of completion",
  ],
  curriculum: baseCurriculum,
  lessons: baseCurriculum.flatMap((section) =>
    section.lessons.map((lesson) => lesson.title)
  ),
  reviews: baseReviews,
});

